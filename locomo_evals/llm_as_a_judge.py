import argparse
import json
import os
from collections import defaultdict
import numpy as np
import ollama
from pydantic import BaseModel

# 1. Define the strict JSON schema for the Judge
class EvaluationResponse(BaseModel):
    reasoning: str
    label: str  # The prompt will instruct it to be "CORRECT" or "WRONG"

ACCURACY_PROMPT = """
Your task is to label an answer to a question as 'CORRECT' or 'WRONG'. You will be given:
    (1) a question
    (2) a 'gold' (ground truth) answer
    (3) a generated answer

The gold answer will usually be a concise and short answer.
The generated answer might be much longer, but you should be generous with your grading - as long as it touches on the same topic and contains the core truth of the gold answer, it should be counted as CORRECT. 

For time-related questions, the gold answer will be a specific date/month. As long as the generated answer refers to the same date or time period, count it as CORRECT.

Question: {question}
Gold answer: {gold_answer}
Generated answer: {generated_answer}

First, provide a short (one sentence) explanation of your reasoning.
Then, assign the label as exactly "CORRECT" or "WRONG".
"""

_DEFAULT_OLLAMA_JUDGE = os.getenv("OLLAMA_JUDGE_MODEL", os.getenv("OLLAMA_MODEL", "qwen3.5:4b"))


def evaluate_llm_judge(
    question: str,
    gold_answer: str,
    generated_answer: str,
    model_name: str | None = None,
) -> int:
    """Evaluate the generated answer against the gold answer using local Ollama."""
    if model_name is None:
        model_name = _DEFAULT_OLLAMA_JUDGE
    prompt = ACCURACY_PROMPT.format(
        question=question, 
        gold_answer=gold_answer, 
        generated_answer=generated_answer
    )
    
    try:
        response = ollama.chat(
            model=model_name,
            messages=[{'role': 'user', 'content': prompt}],
            format=EvaluationResponse.model_json_schema(),
            options={'temperature': 0.0}  # 0.0 is critical for deterministic grading
        )
        
        # Parse the JSON output
        result = EvaluationResponse.model_validate_json(response.message.content)
        
        # Return 1 for CORRECT, 0 for WRONG
        return 1 if result.label.strip().upper() == "CORRECT" else 0
        
    except Exception as e:
        print(f"Error during evaluation: {e}")
        return 0  # Default to wrong if the model crashes

def main():
    parser = argparse.ArgumentParser(description="Evaluate Memory OS results using local LLM judge")
    parser.add_argument(
        "--input_file",
        type=str,
        default="results/locomo_results.json",
        help="Path to the generated results file from eval_locomo.py",
    )
    args = parser.parse_args()

    dataset_path = args.input_file
    output_path = f"scored_{dataset_path.split('/')[-1]}"

    print(f"Loading results from {dataset_path}...\n")
    try:
        with open(dataset_path, "r") as f:
            data = json.load(f)
    except FileNotFoundError:
        print(f"Error: {dataset_path} not found. Run eval_locomo.py first!")
        return

    LLM_JUDGE = defaultdict(list)
    SCORED_RESULTS = []

    # Iterate through the flat list generated by our previous script
    for index, item in enumerate(data):
        question = item["question"]
        gold_answer = item["ground_truth"]
        generated_answer = item["generated_answer"]
        q_type = item["type"]  # e.g., 'single-hop', 'multi-hop'

        # Evaluate the answer
        print(f"Evaluating Q{index + 1} ({q_type})...")
        score = evaluate_llm_judge(question, gold_answer, generated_answer)
        LLM_JUDGE[q_type].append(score)

        # Store the result
        item["llm_label"] = "CORRECT" if score == 1 else "WRONG"
        SCORED_RESULTS.append(item)

        # Print running accuracy
        print("Running accuracy:")
        for cat, results in LLM_JUDGE.items():
            print(f"  {cat}: {np.mean(results):.4f} ({sum(results)}/{len(results)})")
        print("-" * 40)

    # Save final scored results
    with open(output_path, "w") as f:
        json.dump(SCORED_RESULTS, f, indent=4)

    # Print final summary table
    print("\n" + "="*40)
    print("FINAL EVALUATION SUMMARY")
    print("="*40)
    total_correct = 0
    total_questions = 0
    
    for category, scores in LLM_JUDGE.items():
        cat_mean = np.mean(scores)
        cat_sum = sum(scores)
        cat_len = len(scores)
        total_correct += cat_sum
        total_questions += cat_len
        print(f"{category.upper():<15} : {cat_mean:.2%} ({cat_sum}/{cat_len})")
        
    print("-" * 40)
    print(f"{'TOTAL':<15} : {total_correct/total_questions:.2%} ({total_correct}/{total_questions})")
    print("="*40)
    print(f"Detailed scored results saved to: {output_path}")

if __name__ == "__main__":
    main()